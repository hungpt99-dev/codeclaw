const mockRun = {
  id: "run_123",
  title: "Add login page",
  requirement: "Add user login page",
  mode: "docs-only",
  status: "REPORT_GENERATED",
};

beforeEach(() => {
  vi.clearAllMocks();
});

describe("prSummaryGenerator", () => {
  it("generates PR summary without artifacts", async () => {
    mockReadFile.mockRejectedValue(new Error("not found"));

    const summary = await generatePRSummary("run_123", mockPaths, mockRun);
    expect(summary.title).toBe("[CodeClaw] Add login page");
    expect(summary.body).toContain("run_123");
    expect(summary.body).toContain("Generated by CodeClaw");
  });

  it("includes requirement section when available", async () => {
    mockReadFile
      .mockResolvedValueOnce("# Clarified Requirement\nUser can login")
      .mockRejectedValueOnce(new Error("not found"))
      .mockRejectedValueOnce(new Error("not found"))
      .mockRejectedValueOnce(new Error("not found"))
      .mockRejectedValueOnce(new Error("not found"));

    const summary = await generatePRSummary("run_123", mockPaths, mockRun);
    expect(summary.title).toBe("[CodeClaw] Add login page");
    expect(summary.body).toContain("Requirement");
    expect(summary.body).toContain("User can login");
  });

  it("includes changed files section", async () => {
    mockReadFile
      .mockRejectedValueOnce(new Error("not found"))
      .mockResolvedValueOnce(JSON.stringify({ files: ["src/login.tsx", "src/auth.ts"] }))
      .mockRejectedValueOnce(new Error("not found"))
      .mockRejectedValueOnce(new Error("not found"))
      .mockRejectedValueOnce(new Error("not found"));

    const summary = await generatePRSummary("run_123", mockPaths, mockRun);
    expect(summary.body).toContain("login.tsx");
    expect(summary.body).toContain("auth.ts");
  });

  it("includes traceability section", async () => {
    mockReadFile
      .mockRejectedValueOnce(new Error("not found"))
      .mockRejectedValueOnce(new Error("not found"))
      .mockRejectedValueOnce(new Error("not found"))
      .mockRejectedValueOnce(new Error("not found"))
      .mockResolvedValueOnce("## Traceability\n| REQ-001 | Covered |");

    const summary = await generatePRSummary("run_123", mockPaths, mockRun);
    expect(summary.body).toContain("Traceability");
  });
});
